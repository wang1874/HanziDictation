import { Word, Lesson } from "../types";

// 生成唯一ID的辅助函数
function generateId(): string {
  return Math.random().toString(36).substring(2, 15);
}

// 创建Word对象的辅助函数
function createWord(
  text: string,
  pinyin: string,
  type: 'character' | 'word' = 'character',
  grade?: number
): Word {
  return {
    id: generateId(),
    text,
    pinyin,
    type,
    grade,
  };
}

